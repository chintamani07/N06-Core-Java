// program to demonstrate RELATIONAL operator
// < > => <=
package org.tnsif.operator;
// class declaration
public class RelationalOperator {
// 	main method
	public static void main(String[] args) {
		System.out.println(5==8); //equal to
		System.out.println(2!=3); //not equal to
		System.out.println(10>4); //greater than
		System.out.println(2>=2); //greater than equal to
		System.out.println(24<=4); //less than equal to		
	}
}