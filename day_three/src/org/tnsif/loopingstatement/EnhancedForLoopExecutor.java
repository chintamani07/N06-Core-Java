// program to demonstrate on Enhanced FOR-LOOP Executor
package org.tnsif.loopingstatement;
//class declaration
public class EnhancedForLoopExecutor {
//main method
	public static void main(String[] args) {
		char c[] = {'d','T','r'};
//		to print ASCII value of a character
		for(int i:c)
			System.out.println(i+" ");
	}
}