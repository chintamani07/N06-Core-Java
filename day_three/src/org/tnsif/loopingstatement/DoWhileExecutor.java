// program to demonstrate on DO-WHILE Executor
package org.tnsif.loopingstatement;
//class declaration
public class DoWhileExecutor {
//	main method
	public static void main(String[] args) {
		int i=12;
		//exit controlled
		do {
			System.out.println("The Value of i is : "+i);
			i++;
		}while(i<12);
	}
}