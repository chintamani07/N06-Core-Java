// program to demonstrate PREFIX & POSTFIX OPERATOR
// ++ --
package org.tnsif.operator;
//class declaration
public class IncrementDecrement {
//	main method
	public static void main(String[] args) {
		int a=13;
		System.out.println(a);
		++a; //14
		System.out.println(a);
		a++; //15
		System.out.println(a);
		--a; //14
		System.out.println(a);
		a--; //13
		System.out.println(a);
	}
}